/* ============================================================
   PhytClust – session.js
   Save & restore the user's session as a small portable JSON.
   What's included: sidebar form values, appearance state, selected k,
   and (optionally) the Newick tree.
   What's NOT included: API result, cluster maps, scores — those are
   regenerated by Run, including them would inflate the file 100×.
   ============================================================ */

import { state } from "./state.js";

export const SESSION_VERSION = 1;
const NEWICK_EMBED_LIMIT_KB = 500;

/**
 * Mapping of session-JSON paths -> DOM input ids. Each entry covers a single
 * form field. New params should just add a row here, not new code.
 *
 * type: how to read/write the input
 *   "text"     — value as-is, empty string -> null
 *   "int"      — parseInt, NaN -> null
 *   "float"    — parseFloat, NaN -> null
 *   "checkbox" — boolean
 *   "select"   — option value as string
 */
const PARAM_FIELDS = [
  // mode-specific
  { path: "params.k", id: "extra-k", type: "int" },
  { path: "params.top_n", id: "extra-topn", type: "int" },
  { path: "params.num_bins", id: "extra-bins", type: "int" },

  // rooting
  { path: "params.outgroup", id: "extra-outgroup", type: "text" },
  { path: "params.root_taxon", id: "extra-root-taxon", type: "text" },

  // bounds
  { path: "params.max_k", id: "extra-maxk", type: "int" },
  { path: "params.max_k_limit", id: "extra-maxklimit", type: "float" },
  { path: "params.min_cluster_size", id: "extra-min-cluster-size", type: "int" },

  // outlier
  { path: "params.outlier.enabled", id: "extra-outlier", type: "checkbox" },
  { path: "params.outlier.size_threshold", id: "extra-outlier-threshold", type: "int" },
  { path: "params.outlier.prefer_fewer", id: "extra-outlier-prefer-fewer", type: "checkbox" },
  { path: "params.outlier.ratio_mode", id: "extra-outlier-ratio-mode", type: "select" },

  // peak
  { path: "params.peak.lambda_weight", id: "extra-lambda", type: "float" },
  { path: "params.peak.ranking_mode", id: "extra-ranking-mode", type: "select" },
  { path: "params.peak.min_prominence", id: "extra-min-prominence", type: "float" },
  { path: "params.peak.relative_prominence", id: "extra-relative-prom", type: "checkbox" },

  // polytomy
  { path: "params.polytomy.optimize", id: "extra-optimize-polytomies", type: "checkbox" },
  { path: "params.polytomy.no_split_zero", id: "extra-no-split-zero", type: "checkbox" },

  // branch support
  { path: "params.support.use", id: "extra-use-support", type: "checkbox" },
  { path: "params.support.min_support", id: "extra-min-support", type: "float" },
  { path: "params.support.support_weight", id: "extra-support-weight", type: "float" },

  // advanced
  { path: "params.compute_all_clusters", id: "extra-compute-all", type: "checkbox" },
];

function getPath(obj, path) {
  const parts = path.split(".");
  let cur = obj;
  for (const k of parts) {
    if (cur == null) return undefined;
    cur = cur[k];
  }
  return cur;
}

function setPath(obj, path, value) {
  const parts = path.split(".");
  let cur = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    if (cur[parts[i]] == null || typeof cur[parts[i]] !== "object") {
      cur[parts[i]] = {};
    }
    cur = cur[parts[i]];
  }
  cur[parts[parts.length - 1]] = value;
}

function readField(field) {
  const el = document.getElementById(field.id);
  if (!el) return null;
  switch (field.type) {
    case "text": {
      const v = (el.value || "").trim();
      return v ? v : null;
    }
    case "int": {
      const v = parseInt(el.value, 10);
      return Number.isFinite(v) ? v : null;
    }
    case "float": {
      const v = parseFloat(el.value);
      return Number.isFinite(v) ? v : null;
    }
    case "checkbox":
      return !!el.checked;
    case "select":
      return el.value;
    default:
      return null;
  }
}

function writeField(field, value) {
  const el = document.getElementById(field.id);
  if (!el) return;
  switch (field.type) {
    case "text":
    case "select":
      el.value = value == null ? "" : String(value);
      break;
    case "int":
    case "float":
      el.value = value == null ? "" : String(value);
      break;
    case "checkbox":
      el.checked = !!value;
      break;
  }
  // Notify any listeners (mode selector, validation, etc.)
  el.dispatchEvent(new Event("input", { bubbles: true }));
  el.dispatchEvent(new Event("change", { bubbles: true }));
}

function readMode() {
  const active = document.querySelector("#mode-selector .mode-btn.active");
  return active ? active.dataset.mode : "global";
}

function writeMode(mode) {
  if (!mode) return;
  const btn = document.querySelector(
    `#mode-selector .mode-btn[data-mode="${mode}"]`,
  );
  if (btn) btn.click();
}

/**
 * Build a session object from the current UI + state. Excludes API results
 * and cluster maps — those are regenerated by Run.
 *
 * @param {Object} opts
 * @param {boolean} [opts.includeNewick=true] — embed the Newick tree
 */
export function collectSession({ includeNewick = true } = {}) {
  const session = {
    version: SESSION_VERSION,
    saved_at: new Date().toISOString(),
    params: { mode: readMode() },
    appearance: JSON.parse(JSON.stringify(state.render)),
    view: {},
  };

  for (const field of PARAM_FIELDS) {
    setPath(session, field.path, readField(field));
  }

  if (includeNewick) {
    const newickEl = document.getElementById("newick-input");
    const raw = newickEl && newickEl.value ? newickEl.value.trim() : "";
    if (raw) session.newick = raw;
  }

  const sel = document.getElementById("cluster-select");
  if (sel && sel.value) {
    const k = parseInt(sel.value, 10);
    if (Number.isFinite(k)) session.view.selected_k = k;
  }

  return session;
}

/**
 * Apply a previously saved session: write all sidebar inputs + appearance,
 * (optionally) restore the Newick. Does NOT auto-run; the user clicks Run.
 *
 * Returns { applied, errors, hadNewick }.
 */
export function applySession(session) {
  const errors = [];
  if (!session || typeof session !== "object") {
    return { applied: 0, errors: ["Not a JSON object."], hadNewick: false };
  }
  if (session.version && session.version > SESSION_VERSION) {
    errors.push(
      `Session was saved by a newer version (v${session.version}); some settings may not apply.`,
    );
  }

  let applied = 0;
  if (session.params) {
    if (session.params.mode) writeMode(session.params.mode);
    for (const field of PARAM_FIELDS) {
      const v = getPath(session, field.path);
      if (v !== undefined) {
        writeField(field, v);
        applied++;
      }
    }
  }

  if (session.appearance && typeof session.appearance === "object") {
    const merge = (dst, src) => {
      for (const [k, v] of Object.entries(src)) {
        if (v && typeof v === "object" && !Array.isArray(v)) {
          if (!dst[k] || typeof dst[k] !== "object") dst[k] = {};
          merge(dst[k], v);
        } else {
          dst[k] = v;
        }
      }
    };
    merge(state.render, session.appearance);
  }

  let hadNewick = false;
  if (typeof session.newick === "string" && session.newick.trim()) {
    const newickEl = document.getElementById("newick-input");
    if (newickEl) {
      newickEl.value = session.newick.trim();
      hadNewick = true;
    }
  }

  return { applied, errors, hadNewick };
}

export function sessionSizeWarning(session) {
  if (!session.newick) return null;
  const kb = new TextEncoder().encode(session.newick).length / 1024;
  return kb > NEWICK_EMBED_LIMIT_KB
    ? `Embedded Newick is ${kb.toFixed(0)} KB. Consider saving without the tree.`
    : null;
}

export function isSessionJson(text) {
  try {
    const parsed = JSON.parse(text);
    return parsed && typeof parsed === "object" && parsed.version != null && parsed.params;
  } catch {
    return false;
  }
}
